// helper to keep the list terse
function mk(
  n: number,
  cat: StrategyCategory,
  name: string,
  agent: string,
  pair: string,
  tf: string,
  session: string,
  indicators: string[],
  entry: string[],
  exit: string[],
  slPips: number,
  rr: number,
  riskPct: number,
  winRate: number,
  trades: number,
  pnl: number,
  dd: number,
  sharpe: number,
  monthlyAvg: string,
  status: Strategy["status"],
  warning?: string,
): Strategy {
  return {
    id: `S-${String(n).padStart(3, "0")}`,
    name, category: cat, agent, pair, timeframe: tf, session,
    indicators, entryRules: entry, exitRules: exit,
    slPips, tpPips: Math.round(slPips * rr), rr, riskPct,
    maxDailyLossPct: Math.max(3, riskPct * 3),
    maxConcurrent: riskPct >= 5 ? 1 : riskPct >= 2 ? 2 : 4,
    winRate, trades, pnl, dd, sharpe, monthlyAvg, status, warning,
  };
}

