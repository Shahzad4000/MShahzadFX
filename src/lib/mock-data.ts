// small deterministic PRNG so counts stay stable across reloads
function rng(seed: number) {
  let s = seed >>> 0;
  return () => {
    s = (s * 1664525 + 1013904223) >>> 0;
    return s / 0xffffffff;
  };
}
function pick<T>(arr: T[], r: () => number) { return arr[Math.floor(r() * arr.length)]; }

